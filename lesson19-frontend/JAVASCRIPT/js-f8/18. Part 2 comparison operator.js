// == 
// ===
// !==

var a = 1;
var b = 1;

console.log(a == b); // true

var c = '1';

console.log(a == c); // true -> chỉ quan tâm đến value

console.log(a === c); // false -> quan tâm đến cả value và data type

console.log(a != b); // false
console.log(a !== c); // true





